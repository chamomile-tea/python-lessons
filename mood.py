mood = input("What is your mood? ")

if mood == "nervous":
    print("Take a deep breath 3 times. ")
elif mood == "happy":
    print("It's great to see you happy! ")
elif mood == "sad":
    print("Brew a cup of happy tea. ")
elif mood == "excited":
    print("Go dancing! ")
elif mood == "relaxed":
    print("Treat yourself with some wine and chocolate. ")
else:
    print("I don't recognize this mood. ")
